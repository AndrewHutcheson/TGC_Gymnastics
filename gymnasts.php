									<p>
										<ol>
											<li>
												<strong>Which colleges have clubs that are members of the TGC?</strong></br>
												Collegiate teams in Texas are automatically inducted within the TGC. Currently, the largest programs in the state are from Baylor University, Texas A&M University, Texas State University, Texas Tech University, 
												The University of Texas and The University of Texas at Dallas. However, membership is always open to those students at smaller schools without large teams (or even teams of one). In the past few years, gymnasts 
												from Austin Community College, McMurray University, Eastfield College of Dallas, and Tarrant County Community College have competed with the TGC, just to name a few. Beginning with the 2014 to 2015 season, 
												teams from the University of Oklahoma and Tulane University have been inducted into the organization.
											</li>
											<li>
												<strong>How do I start a club at my college?</strong></br>
												If you are looking to start a gymnastics club at your college, you will first have to follow your university’s procedures for creating a new student organization. Contact the recreation or the sport clubs department, 
												or the head of the department of student activities or organizations. Also, <a href = "contactUs.php">send us an email</a> so that once you have formed a club, you can start attending TGC competitions and possibly 
												join the conference.
											</li>
											<li>
												<strong>How can I raise money for my club?</strong></br>
												Clubs are funded in a variety of different ways.  Some clubs receive an allocation from their sport clubs department, some are supported by donations from club alumni, and some rely mostly on their own 
												fund-raising efforts.  Many TGC clubs hold “flips for tips” fund-raisers before home football games to receive donations. Additionally, letter drives and hosting parent's night out events have proven to be 
												successful fund-raisers for member clubs.
											</li>
											<li>
												<strong>What level do TGC members compete?</strong></br>
												Women compete under modified level 9 rules, while men compete under modified FIG rules. The women’s rules can be found at http://www.naigc.net/RulesWA.php. The men's rules are identical to NCAA rules, with the added
												exception that they can raise the height of parallel bars if the individual is too tall.
											</li>
											<li>
												<strong>So do I have to be a level 9 or 10 gymnast to compete?</strong></br>
												No, TGC welcomes competitors of all skill levels, from beginner to elite, to compete in a positive, encouraging environment. Many of our most active members began the sport in college.
											</li>
											<li>
												<strong>How many competitions are there in a typical season?</strong></br>
												In a typical season there will be at least five competitions, usually more, plus the TGC Conference Championships, which is normally held the last weekend in March. In addition, most clubs send teams to the 
												NAIGC National Championships in April.
											</li>
											<li>
												<strong>I am a high school student / parent interested in competing gymnastics in college.</strong></br>
												Awesome! Contact the school or schools that you are interested in and they can offer some information on how they practice and run their organization. If you want to go to a college without a program, then you can 
												always compete as an individual from that school, or better yet, let us know and we can help you start one.
											</li>
											<li>
												<strong>How much does it cost?</strong></br>
												The TGC regulates the entry fees for it's meets. This helps keep costs down for all clubs involved and provides financial stability for the clubs by keeping expectations the same. The entry fees are $10/gymnast and
												$20/team per meet. Most clubs pay this out of their finances. To see what various clubs charge for membership dues, check out the <a href = "teams.php">school information page</a>.
											</li>
											<li>
												<strong>Does the TGC offer scholarships?</strong></br>
												Not at this time. However, the <a href = "http://www.simsscholarship.org/">Sims Scholarship</a> is available for men.
											</li>
